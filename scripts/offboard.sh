#!/usr/bin/env bash
# scripts/offboard.sh — yashigani offboard <agent-name>
#
# Reverses the codegen output for a named onboarded agent:
#   1. Removes the service_identities.yaml entry (sentinel-guarded)
#   2. Removes the pki_ownership.sh tuple (sentinel-guarded, via Python rewriter)
#   3. Removes the Caddy include LINE from docker/caddy/agents/ (include line only —
#      does NOT restructure Caddyfile internals; S4 sentinel-insert is Captain's scope)
#   4. Removes the compose override stanza (docker/<agent>-compose.override.yml)
#   5. Wipes secrets/certs: <agent>_client.{key,crt}
#   6. Triggers _pki_run_issuer rotate-leaves to rotate remaining service leaves
#   7. Removes the Helm values block (values-<agent>.yaml + networkpolicy overlay)
#   8. Writes an offboard ledger entry (docker/var/offboard-ledger.log)
#
# Each step is idempotent — safe to re-run if a previous run was interrupted.
#
# S4 TODO (Captain scope): Caddyfile sentinel-insert/remove for the import line
# lives in the Captain parallel branch (feat/p1-w3p2a-caddy-hardening). This
# script removes the include FILE (agents/<agent>.caddy) and the import LINE
# within whichever Caddyfile contains it, using an exact-string match. Do NOT
# restructure Caddyfile blocks or change the surrounding block layout.
#
# Security:
#   - Three-tier file removal: direct → podman unshare → ephemeral container
#     (mirrors uninstall.sh pattern, avoid EPERM on UID-remapped secrets)
#   - Never uses sed -i (BSD/GNU portability trap)
#   - Fail-closed: any required step failing exits non-zero
#   - Idempotent: re-running after partial completion is safe
#
# Usage:
#   ./scripts/offboard.sh <agent-name>
#   YSG_RUNTIME=docker ./scripts/offboard.sh my-agent
#   YSG_DRY_RUN=true ./scripts/offboard.sh my-agent
#
# Requirements:
#   - Run from the repo root (or WORK_DIR set)
#   - install.sh must have been run previously (WORK_DIR must exist)
#   - python3 available for lib/pki_ownership_append.py calls
#
# Bash 3.2 compatibility: no declare -A, no ${var,,}, no mapfile.
#
# Last updated: 2026-05-29T00:00:00+00:00 (feat(p1-w4): S5 yashigani offboard command)
# ---------------------------------------------------------------------------

set -euo pipefail
IFS=$(printf '\n\t')

# Hardened PATH — never trust inherited PATH for privileged scripts.
PATH=/opt/homebrew/bin:/opt/homebrew/sbin:/opt/podman/bin:/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin
export PATH

# ---------------------------------------------------------------------------
# Globals
# ---------------------------------------------------------------------------
_SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
_REPO_ROOT="$(cd "${_SCRIPT_DIR}/.." && pwd)"

WORK_DIR="${WORK_DIR:-${_REPO_ROOT}}"
YSG_RUNTIME="${YSG_RUNTIME:-}"
YSG_DRY_RUN="${YSG_DRY_RUN:-false}"

# Alpine image for ephemeral-container tier (digest-pinned, same as install.sh).
_ALPINE_IMAGE="alpine:3@sha256:5b10f432ef3da1b8d4c7eb6c487f2f5a8f096bc91145e68878dd4a5019afde11"

# ---------------------------------------------------------------------------
# Logging helpers (mirror install.sh style)
# ---------------------------------------------------------------------------
_log_info()  { printf "  [offboard] %s\n"        "$*"; }
_log_warn()  { printf "  [offboard] WARN: %s\n"  "$*" >&2; }
_log_error() { printf "  [offboard] ERROR: %s\n" "$*" >&2; }
_log_step()  { printf "\n  [offboard] ── %s\n"   "$*"; }
_dry_print() {
  if [[ "${YSG_DRY_RUN:-false}" == "true" ]]; then
    printf "  [DRY-RUN] %s\n" "$*"
    return 0
  fi
  return 1  # not dry run — caller should execute the real command
}

# ---------------------------------------------------------------------------
# _offboard_usage
# ---------------------------------------------------------------------------
_offboard_usage() {
  cat >&2 <<'USAGE'
Usage: scripts/offboard.sh <agent-name>

  Removes all artifacts generated by `yashigani onboard` for the named agent:
    service_identities.yaml entry, pki_ownership.sh tuple, Caddy snippet,
    compose override, certs/keys, Helm values, and writes an offboard ledger entry.

Options (env vars):
  YSG_RUNTIME=docker|podman    — override runtime detection
  YSG_DRY_RUN=true             — print actions without executing
